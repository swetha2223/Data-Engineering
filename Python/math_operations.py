#math_operations
#add
def add (a,b):
    return a + b

#sub
def subtraction (a,b):
    return a - b

#multiply
def multiplication (a,b):
    return a * b

# division
def division(a,b):
    if b == 0:
        return "Cannot divide by Zero!"
    return a / b

# modulus
def modulus(a,b):
    return a // b
