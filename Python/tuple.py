# create a tuple of colours

colors = ("red", "green", "blue")

#accessing th element of tuple

print("first colour:", colors[0])
print("last colour:", colors[-1])

#length of tuple
tuple_length =len(colors)
print("length of the tuple:", tuple_length)

# looping through the tuples
print("tuple elements:")
for color in colors:
    print(color)

